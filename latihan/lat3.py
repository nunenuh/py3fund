# tipe data dan variabel

a = 'ini text' # tipe data string
print("Tipe Data Variabel A : ",type(a))

b = 10 # tipe data integer
print("Tipe Data Variabel B : ",type(b))

zz = float(b)
print("Tipe Data Variabel ZZ : ",type(zz))

c = 7.10 # tipe data float
print("Tipe Data Variabel C : ", type(c))

xx = int(c)
print("Tipe Data Variabel XX : ", type(xx))

d = [1,2,3] # tipe data list
print("Tipe Data Variabel D : ",type(d))

e = (2,3,4) # tipe data tupple
print("Tipe Data Variabel E : ",type(e))

f = {'nilai':78} # tipe data dictionary
print("Tipe Data Variabel F : ",type(f))

